    <p>Here are some aphorisms that I penned this year.</p>

    <blockquote>
        <p>
       	Nothing is more majestic than dreams that can't come true and nothing more mundane than those that do.
        </p>
    </blockquote>
    <p>
        The dream that will never come true, that can never see the light of a real sun on a day that is perhaps a bit too hot, now that is a dream that can stay with you for a lifetime. But alas, the dream realized is forever gone and in its realization it is whisped away into the past to be forgotten.
    </p>

    <blockquote>
        <p>The only man who knows anything is the one who knows he knows absolutely nothing.</p>
    </blockquote>

    <p>Only those who have learned for a lifetime have had the possibility of glancing out at the valley of all that we do not know. And only they have seen that that valley is not a valley at all but as vast and endless as the very fabric of our universe.</p>

    <blockquote>
        <p>The question is the only animal which gives birth to itself.</p>
    </blockquote>
    <p>I was pleasantly suprised to be introduced to a curious creature this year, he or she (or perhaps the more appropriately placed they) give birth to themselves, spawning more of their own ilk, much faster than rabbits it seems.</p>

    <blockquote>
        <p>Answers are the gifts that questions give.</p>
    </blockquote>

    <p>Those curiously fascinating self-made creatures can be found hanging around another critter - the answer. These are an eclectic sort. They can be quite long lived or trite and prone to bite. Any time you want to summon a few, ask people questions about themselves, be forewarned though, this has been known to spawn copious amounts of the little buggers.</p>

